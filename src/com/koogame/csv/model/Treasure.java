package com.koogame.csv.model;

public class Treasure {
	private int type;
	private String name;
	private int hero1;
	private int hero2;
	private int hero3;

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getHero1() {
		return hero1;
	}

	public void setHero1(int hero1) {
		this.hero1 = hero1;
	}

	public int getHero2() {
		return hero2;
	}

	public void setHero2(int hero2) {
		this.hero2 = hero2;
	}

	public int getHero3() {
		return hero3;
	}

	public void setHero3(int hero3) {
		this.hero3 = hero3;
	}
}
